package com.example.superlig;

public class Takim {
    private String takim;
    private int logo;

    public Takim(String takim, int logo) {
        this.takim = takim;
        this.logo = logo;
    }

    public String getTakim() {
        return takim;
    }

    public void setTakim(String takim) {
        this.takim = takim;
    }

    public int getLogo() {
        return logo;
    }

    public void setLogo(int logo) {
        this.logo = logo;
    }
}
